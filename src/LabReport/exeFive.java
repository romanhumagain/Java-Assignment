package LabReport;

public class exeFive {

	public static void main(String[] args) {
		
		
		// create variables
	    int a = 7, b = 6;

	    // Displaying the assigned values
	    System.out.println("a is " + a + " and b is " + b);

	    // == operator (left value is equal to right value)
	    System.out.println(a == b);  // false

	    // != operator (left value is not equal to right value)
	    System.out.println(a != b);  // true

	    // > operator (left value is greater than right value)
	    System.out.println(a > b);  // false

	    // < operator (left value is less than right value)
	    System.out.println(a < b);  // true

	    // >= operator (left value is greater than or equal to right value)
	    System.out.println(a >= b);  // false

	    // <= operator (left value is less than or equal to right value)
	    System.out.println(a <= b);  // true

	}

}
